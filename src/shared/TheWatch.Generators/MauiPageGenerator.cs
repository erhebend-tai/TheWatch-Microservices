    private static void EmitProgramRoutes(SourceProductionContext ctx, string rootNs)
    {
        var sb = new StringBuilder();
        sb.AppendLine("// <auto-generated/>");
        sb.AppendLine("// API route constants generated by TheWatch.Generators.MauiPageGenerator");
        sb.AppendLine("#nullable enable");
        sb.AppendLine();
        sb.AppendLine($"namespace {rootNs}.Generated;");
        sb.AppendLine();
        sb.AppendLine("/// <summary>API route constants for all TheWatch programs.</summary>");
        sb.AppendLine("public static class ProgramRoutes");
        sb.AppendLine("{");

        foreach (var p in Programs)
        {
            sb.AppendLine($"    public static class {p.Id}");
            sb.AppendLine("    {");
            sb.AppendLine($"        public const string ServiceName = \"{p.Name}\";");
            sb.AppendLine($"        public const int Port = {p.Port};");
            sb.AppendLine($"        public const string BaseUrl = \"http://localhost:{p.Port}\";");
            sb.AppendLine($"        public const string Health = \"/health\";");
            sb.AppendLine($"        public const string Info = \"/info\";");
            sb.AppendLine("    }");
            sb.AppendLine();
        }

        sb.AppendLine("}");

        ctx.AddSource("ProgramRoutes.g.cs", SourceText.From(sb.ToString(), Encoding.UTF8));
    }
