    public void Initialize(IncrementalGeneratorInitializationContext context)
    {
        var projectInfo = context.AnalyzerConfigOptionsProvider
            .Select((opts, _) =>
            {
                opts.GlobalOptions.TryGetValue("build_property.RootNamespace", out var ns);
                opts.GlobalOptions.TryGetValue("build_property.UseMaui", out var useMaui);
                return (Namespace: ns ?? "", UseMaui: useMaui ?? "");
            });

        context.RegisterSourceOutput(projectInfo, (spc, info) =>
        {
            // Only emit for MAUI projects
            if (!info.UseMaui.Equals("true", System.StringComparison.OrdinalIgnoreCase) &&
                !info.Namespace.Contains("Mobile"))
                return;

            EmitProgramClients(spc, info.Namespace);
            EmitProgramRoutes(spc, info.Namespace);
        });
    }

    private static void EmitProgramClients(SourceProductionContext ctx, string rootNs)
    {
        var sb = new StringBuilder();
        sb.AppendLine("// <auto-generated/>");
        sb.AppendLine("// Per-program HTTP clients generated by TheWatch.Generators.MauiPageGenerator");
        sb.AppendLine("#nullable enable");
        sb.AppendLine();
        sb.AppendLine("using System.Net.Http;");
        sb.AppendLine("using System.Net.Http.Json;");
        sb.AppendLine("using System.Threading.Tasks;");
        sb.AppendLine("using System.Collections.Generic;");
        sb.AppendLine();
        sb.AppendLine($"namespace {rootNs}.Generated;");
        sb.AppendLine();

        foreach (var p in Programs)
        {
            sb.AppendLine($"/// <summary>Typed HTTP client for {p.Id} {p.Name}.</summary>");
            sb.AppendLine($"public class {p.Id}Client");
            sb.AppendLine("{");
            sb.AppendLine("    private readonly HttpClient _http;");
            sb.AppendLine($"    public const string ServiceName = \"{p.Id}-{p.Name.ToLowerInvariant()}\";");
            sb.AppendLine($"    public const int DefaultPort = {p.Port};");
            sb.AppendLine();
            sb.AppendLine($"    public {p.Id}Client(HttpClient http) => _http = http;");
            sb.AppendLine();

            // Health check — universal
            sb.AppendLine($"    public async Task<bool> CheckHealthAsync(string baseUrl = \"http://localhost:{p.Port}\")");
            sb.AppendLine("    {");
            sb.AppendLine("        try");
            sb.AppendLine("        {");
            sb.AppendLine("            var r = await _http.GetAsync($\"{baseUrl}/health\");");
            sb.AppendLine("            return r.IsSuccessStatusCode;");
            sb.AppendLine("        }");
            sb.AppendLine("        catch { return false; }");
            sb.AppendLine("    }");
            sb.AppendLine();

            // Info endpoint — universal
            sb.AppendLine($"    public async Task<object?> GetInfoAsync(string baseUrl = \"http://localhost:{p.Port}\")");
            sb.AppendLine("    {");
            sb.AppendLine("        return await _http.GetFromJsonAsync<object>($\"{baseUrl}/info\");");
            sb.AppendLine("    }");

            // Program-specific endpoints
            EmitProgramSpecificMethods(sb, p.Id, p.Port);

            sb.AppendLine("}");
            sb.AppendLine();
        }

        ctx.AddSource("ProgramClients.g.cs", SourceText.From(sb.ToString(), Encoding.UTF8));
    }

