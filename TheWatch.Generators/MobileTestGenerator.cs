/// Scans the service source files and emits test classes with boilerplate
/// for any service that does not yet have a corresponding test file.
/// Targets TheWatch.Mobile.Tests projects (net10.0 TFM).
/// </summary>
[Generator]
public class MobileTestGenerator : IIncrementalGenerator
{
